package org.jlab.epsci.dppui.test;

import org.jlab.epsci.dppui.system.JCGComponent;

import javax.xml.bind.JAXBContext;
import javax.xml.bind.JAXBException;
import javax.xml.bind.Unmarshaller;
import java.io.File;
import java.io.IOException;

public class JaxbReadTest {
    public static void main(String[] args) throws JAXBException, IOException {

        String compName = args[0];

        JAXBContext context = JAXBContext.newInstance(JCGComponent.class);

        Unmarshaller um = context.createUnmarshaller();
        JCGComponent comp = (JCGComponent) um.unmarshal(new File("/home/gurjyan/Test/"+compName+".xml"));
        System.out.println(comp);
    }

}
